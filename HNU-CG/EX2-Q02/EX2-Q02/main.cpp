#include <iostream>
int main() {
    using namespace std;
    int x1, x2, x3, y1, y2, y3;
    char a, b, c, d, e, f, g, h, i, j, k;
    cin >> a >> x1 >> b >> y1 >> c >> d >> e >> x2 >> f >> y2 >> g >> h >> i >> x3 >> j >> y3 >> k;
    cout << "[" << x1 << "," << y1 << "]\n";
    cout << "[" << x2 << "," << y2 << "]\n";
    cout << "[" << x3 << "," << y3 << "]\n";
    return 0;
}
